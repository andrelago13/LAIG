/**
 * Modx
 * @constructor
 */
function Modx() {
	this.gameHistory = [];
};
Modx.prototype.constructor=Modx;

Modx.prototype.start = function() {
	this.gameHistory = [new Game()];
	return this.getGame();
}

Modx.prototype.getGame = function() {
	return this.gameHistory[0];
}